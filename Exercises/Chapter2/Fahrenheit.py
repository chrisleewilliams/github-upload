# Fahrenheit.py
# Program to convert fahrenheit to celsisus
# Author: Chris Williams

def main():
   fahrenheit = eval(input("Enter a temperature in fahrenheit to convert it to celsius: "))
   celsius = 5 / 9 * (fahrenheit - 32)
   print("The temperature in celsius is", celsius)
   input("Press the <Enter> key toe exit the program")

main()


